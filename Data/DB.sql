-- 1. TẠO BẢNG DỮ LIỆU
-------------------------------------------------------

CREATE TABLE NHANVIEN (
    MANV VARCHAR(6) PRIMARY KEY, 
    TENNV NVARCHAR(50) NOT NULL,
    GIOITINH BIT, -- 1: Nam, 0: Nữ
    NGAYVAOLAM DATE DEFAULT GETDATE(),
    SODT VARCHAR(10),
    DIACHI NVARCHAR(100),
    QUANLY BIT DEFAULT 0
);

CREATE TABLE TAIKHOAN (
    TENDANGNHAP VARCHAR(20) PRIMARY KEY,
    MATKHAU VARCHAR(255) NOT NULL DEFAULT '123',
    QUYEN NVARCHAR(20) CHECK (QUYEN IN (N'Quản lý', N'Nhân viên')),
    MANV VARCHAR(6) UNIQUE,
    TRANGTHAI BIT DEFAULT 1,
    CONSTRAINT FK_TK_NV FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV) ON DELETE CASCADE
);

CREATE TABLE KHACHHANG (
    MAKH VARCHAR(6) PRIMARY KEY, 
    TENKH NVARCHAR(50) NOT NULL,
    SODT VARCHAR(10)
);

CREATE TABLE NCC (
    MANCC VARCHAR(6) PRIMARY KEY,
    TENNCC NVARCHAR(50) NOT NULL,
    DIACHI NVARCHAR(100),
    SODIENTHOAI VARCHAR(10)
);

CREATE TABLE SANPHAM (
    MASP VARCHAR(6) PRIMARY KEY,
    TENSP NVARCHAR(50) NOT NULL,
    SOLUONGSP INT DEFAULT 0,
    GIASP FLOAT CHECK (GIASP >= 0),
    LOAISP NVARCHAR(20),
    HINHANH NVARCHAR(255), -- Cột đã được thêm vào đây
    MANCC VARCHAR(6) NULL, 
    CONSTRAINT FK_SP_NCC FOREIGN KEY (MANCC) REFERENCES NCC(MANCC)
);

CREATE TABLE BAN (
    MABAN VARCHAR(6) PRIMARY KEY, 
    TRANGTHAI BIT DEFAULT 1, -- 1: Trống, 0: Có khách
    VITRI NVARCHAR(50)
);

CREATE TABLE PHIEUDATBAN (
    MAPDB VARCHAR(6) PRIMARY KEY,
    NGAYDAT DATE DEFAULT GETDATE(),
    VITRI NVARCHAR(50),
    TRANGTHAI BIT DEFAULT 0,
    MANV VARCHAR(6), 
    MAKH VARCHAR(6),
    CONSTRAINT FK_PDB_NV FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
    CONSTRAINT FK_PDB_KH FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH)
);

CREATE TABLE CT_PHIEU_DB (
    MAPDB VARCHAR(6),
    MABAN VARCHAR(6),
    GHICHU NVARCHAR(100),
    PRIMARY KEY (MAPDB, MABAN),
    CONSTRAINT FK_CTP_P FOREIGN KEY (MAPDB) REFERENCES PHIEUDATBAN(MAPDB),
    CONSTRAINT FK_CTP_B FOREIGN KEY (MABAN) REFERENCES BAN(MABAN)
);

CREATE TABLE HOADON (
    MAHD VARCHAR(6) PRIMARY KEY,
    NGAYLAPHD DATE DEFAULT GETDATE(),
    MANV VARCHAR(6),
    MAKH VARCHAR(6),
    MABAN VARCHAR(6),
    THANHTOAN BIT DEFAULT 0,
    THUEVAT FLOAT DEFAULT 0.1,
    TONGTIEN FLOAT DEFAULT 0,
    MAPDB VARCHAR(6) NULL,
    CONSTRAINT FK_HD_NV FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
    CONSTRAINT FK_HD_KH FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH),
    CONSTRAINT FK_HD_BAN FOREIGN KEY (MABAN) REFERENCES BAN(MABAN),
    CONSTRAINT FK_HD_PDB FOREIGN KEY (MAPDB) REFERENCES PHIEUDATBAN(MAPDB)
);

CREATE TABLE CTHOADON (
    MAHD VARCHAR(6),
    MASP VARCHAR(6),
    SOLUONG INT CHECK (SOLUONG > 0),
    DONGIA FLOAT,
    GHICHU NVARCHAR(100),
    PRIMARY KEY (MAHD, MASP),
    CONSTRAINT FK_CTH_HD FOREIGN KEY (MAHD) REFERENCES HOADON(MAHD),
    CONSTRAINT FK_CTH_SP FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);
GO

-------------------------------------------------------
-- 2. TẠO TRIGGER TỰ ĐỘNG SINH MÃ
-------------------------------------------------------

-- Trigger cho NHANVIEN & TAIKHOAN
CREATE TRIGGER TRG_AUTO_NV ON NHANVIEN INSTEAD OF INSERT AS
BEGIN
    DECLARE @MAX_MA VARCHAR(6), @NEXT_NUM INT, @NEW_MA VARCHAR(6);
    SELECT @MAX_MA = MAX(MANV) FROM NHANVIEN WHERE MANV LIKE 'NV%';
    SET @NEXT_NUM = ISNULL(CAST(RIGHT(@MAX_MA, 4) AS INT), 0) + 1;
    SET @NEW_MA = 'NV' + RIGHT('0000' + CAST(@NEXT_NUM AS VARCHAR), 4);

    INSERT INTO NHANVIEN (MANV, TENNV, GIOITINH, NGAYVAOLAM, SODT, DIACHI, QUANLY)
    SELECT @NEW_MA, TENNV, GIOITINH, ISNULL(NGAYVAOLAM, GETDATE()), SODT, DIACHI, ISNULL(QUANLY, 0) FROM INSERTED;

    INSERT INTO TAIKHOAN (TENDANGNHAP, MATKHAU, QUYEN, MANV, TRANGTHAI)
    SELECT @NEW_MA, '123', CASE WHEN (SELECT QUANLY FROM INSERTED) = 1 THEN N'Quản lý' ELSE N'Nhân viên' END, @NEW_MA, 1 FROM INSERTED;
END;
GO

-- Trigger cho SANPHAM (Đã cập nhật cột HINHANH)
CREATE TRIGGER TRG_AUTO_SP ON SANPHAM INSTEAD OF INSERT AS
BEGIN
    DECLARE @MAX_MA VARCHAR(6), @NEXT_NUM INT, @NEW_MA VARCHAR(6);
    SELECT @MAX_MA = MAX(MASP) FROM SANPHAM WHERE MASP LIKE 'SP%';
    SET @NEXT_NUM = ISNULL(CAST(RIGHT(@MAX_MA, 4) AS INT), 0) + 1;
    SET @NEW_MA = 'SP' + RIGHT('0000' + CAST(@NEXT_NUM AS VARCHAR), 4);

    INSERT INTO SANPHAM (MASP, TENSP, SOLUONGSP, GIASP, LOAISP, HINHANH, MANCC)
    SELECT @NEW_MA, TENSP, SOLUONGSP, GIASP, LOAISP, HINHANH, MANCC FROM INSERTED;
END;
GO

